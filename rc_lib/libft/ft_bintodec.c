#include "libft.h"

int		ft_bintodec(int n)
{
	int a;
	int new;

	new = 0;
	a = 0;
	while (n)
	{
		new += (n % 2) * ft_pow(2, a);
		n /= 10;
		a++;
	}
	return (new);
}
