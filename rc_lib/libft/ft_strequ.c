#include <stdio.h>

int		ft_strequ(char const *s1, char const *s2)
{
	int a;

	if (!s1 || !s2)
		return (0);
	a = 0;
	while (s1[a] || s2[a])
	{
		if (s1[a] != s2[a])
			return (0);
		a++;
	}
	return (1);
}
